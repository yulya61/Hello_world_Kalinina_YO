print("ВИТЯЗЬ НА РАСПУТЬЕ")
print("\nСтоит витязь перед камнем вещим, а на камне том написано:\n")
print("  \"Налево пойдёшь — коня потеряешь, себя спасёшь\"")
print("  \"Направо пойдёшь — себя потеряешь, коня спасёшь\"")
print("  \"Прямо пойдёшь — и себя, и коня потеряешь\"")
print("  \"Назад пойдёшь — ...\" (дальше надпись стёрлась)\n")

choice = input("Куда пойдёшь, витязь? (налево/направо/прямо/назад): ").strip().lower()
if choice == "налево" or choice == "левый" or choice == "л":
    print("\nПоехал витязь налево...")
    print("Повстречал разбойников лихих, бился с ними три дня и три ночи.")
    print("Коня верного зарубили разбойники, а витязь пешим выбрался.")
    print("Выжил, но без коня остался. Домой вернулся, коня нового вырастил.")
    print("\nИТОГ: Себя спас, коня потерял.")

elif choice == "направо" or choice == "правый" or choice == "п":
    print("\nПоехал витязь направо...")
    print("Пришёл к болотам топким, к гнезду Змея Горыныча трёхглавого.")
    print("Бился с ним витязь, сразил все три головы, да сам раны тяжкие получил.")
    print("Упал витязь замертво, а конь верный домой его привёз, да не спасти уж было.")
    print("\nИТОГ: Себя потерял, коня спас.")

elif choice == "прямо" or choice == "прям" or choice == "пр":
    print("\nПоехал витязь прямо...")
    print("А там — тьма беспросветная, чудища невиданные.")
    print("Пропал витязь без вести, и коня его никто больше не видел.")
    print("\nИТОГ: И себя, и коня потерял.")

elif choice == "назад" or choice == "обратно" or choice == "н":
    print("\nПовернул витязь коня назад...")
    print("Домой воротился, к отцу с матерью, к жене молодой.")
    print("Живёт поживает, хозяйство ведёт, детей растит.")
    print("Да только всю жизнь его мысль покоя не даёт: а что бы там было, если бы решился?")
    print("\nИТОГ: Жив остался, но с камнем на душе.")

else:
    print("\nСтоит витязь в недоумении...")
    print("Неведомый путь выбрал он, а такого на камне не писано.")
    print("Так и стоит до сих пор на распутье, век стоит, думу думает...")
    print("\nИТОГ: Никуда не пошёл, так и остался на месте.")